package abstractclass;

public abstract class Animal {
    protected int age;
    protected String name;
    public abstract void makeNoise();

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}